use thiserror::Error;

/// Ошибки доменного слоя — чистые бизнес-концепции.
///
/// DDD-принцип: домен **не знает** ни о sqlx, ни об HTTP, ни о tera.
/// Никаких `#[from] sqlx::Error` здесь быть не должно.
///
/// Инфраструктурные ошибки (обрыв соединения, constraint violation) конвертируются
/// в `Internal(String)` на границе адаптера (infrastructure/postgres/mod.rs),
/// тем самым скрывая детали реализации от верхних слоёв.
///
/// Аналог в Python: кастомные исключения в domain/ без import sqlalchemy.
#[derive(Error, Debug)]
pub enum DomainError {
    /// Запрошенная сущность не найдена.
    /// Это бизнес-факт, а не техническая ошибка — пост с таким slug не существует.
    #[error("Not found: {0}")]
    NotFound(String),

    /// Нарушение уникальности: дубликат slug, email и т.д.
    /// Домен знает о своих инвариантах — slug поста должен быть уникален.
    #[error("Conflict: {0}")]
    Conflict(String),

    /// Невалидные данные: нарушение инвариантов домена (пустой заголовок, слишком длинный контент).
    /// Отличается от BadRequest в HTTP-слое: здесь это именно нарушение бизнес-правила.
    #[error("Validation error: {0}")]
    ValidationError(String),

    /// Действие требует аутентификации или прав доступа.
    #[error("Unauthorized")]
    Unauthorized,

    /// Непредвиденная техническая ошибка инфраструктуры.
    ///
    /// `String` вместо `sqlx::Error` — намеренно. Домен не должен зависеть от деталей БД.
    /// Инфраструктурный адаптер сам конвертирует `sqlx::Error` в этот вариант.
    /// В `into_response` (web-слой) этот вариант логируется и клиенту отдаётся 500.
    #[error("Internal error: {0}")]
    Internal(String),
}
